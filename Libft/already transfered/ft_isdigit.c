#include <stdio.h>

int		ft_isdigit(int c)
{
	if (c >= 48 && c <= 57)
		return (1);
	else
		return (0);
}

int main(void)
{
	char i;

	i = 'a';
	printf("%i", ft_isdigit(i));
}
